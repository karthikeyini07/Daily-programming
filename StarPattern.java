import java.util.*;
class k
{
  public static void main(String[] args)
  {
    Scanner obj=new Scanner(System.in);
    int s=obj.nextInt();
    strptn(s);
    
  }
  public static void strptn(int s)
  {
    for(int i=0;i<s;i++)
    {
      for(int j=s;j<i;j++)
      {
        System.out.print("*");
      }
        
        for(int k=0;k<2*i;k++)
        {
          System.out.print(" ");
        }
        if(i<s-1)
        {
          System.out.print("*");
        }
    }
  }
}
